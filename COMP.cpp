#include <bits/stdc++.h>
#define ll long long
#define TASK "INP"
using namespace std;
#define pq priority_queue <ll>
#define pqd priority_queue <bg,vector<bg>,cmp>
const int N=1e2+5;
int n,i,j;
ll a[N];
ll gcd(ll a,ll b){return __gcd(a,b);} 
ll lcm(ll a,ll b){return a*b/gcd(a,b);}
int main()
{
    if (fopen(TASK".INP","r"))
    {
        freopen(TASK".INP","r",stdin);
        freopen(TASK".OUT","w",stdout);;
    }
    ios_base::sync_with_stdio(0);cin.tie(0);cout.tie(0);
    cin>>n;
    for (i=1;i<=n;i++)
    {
      cin>>a[i];
    }
    // cout<<n<<" "<<*max_element(a+1,a+n+1)<<"\n";
    ll res=a[1],ans=a[1];
    for (i=2;i<=n;i++)
    {
        ans=lcm(res,a[i]);
        res=ans;
    }
    cout<<ans<<"\n";
    for (i=1;i<=n;i++)
    {
        cout<<ans/a[i]<<" ";
    }

}
